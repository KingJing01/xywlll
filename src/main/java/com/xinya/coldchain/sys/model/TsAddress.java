package com.xinya.coldchain.sys.model;

public class TsAddress {
    private String pkAddress;
    private String addrType;
    private String addrCode;
    private String addrName;
    private String pkProvince;
    private String pkCity;
    private String pkArea;
    private String detailAddr;

    public String getPkAddress() {
        return pkAddress;
    }

    public void setPkAddress(String pkAddress) {
        this.pkAddress = pkAddress;
    }

    public String getAddrType() {
        return addrType;
    }

    public void setAddrType(String addrType) {
        this.addrType = addrType;
    }

    public String getAddrCode() {
        return addrCode;
    }

    public void setAddrCode(String addrCode) {
        this.addrCode = addrCode;
    }

    public String getAddrName() {
        return addrName;
    }

    public void setAddrName(String addrName) {
        this.addrName = addrName;
    }

    public String getPkProvince() {
        return pkProvince;
    }

    public void setPkProvince(String pkProvince) {
        this.pkProvince = pkProvince;
    }

    public String getPkCity() {
        return pkCity;
    }

    public void setPkCity(String pkCity) {
        this.pkCity = pkCity;
    }

    public String getPkArea() {
        return pkArea;
    }

    public void setPkArea(String pkArea) {
        this.pkArea = pkArea;
    }

    public String getDetailAddr() {
        return detailAddr;
    }

    public void setDetailAddr(String detailAddr) {
        this.detailAddr = detailAddr;
    }


}
