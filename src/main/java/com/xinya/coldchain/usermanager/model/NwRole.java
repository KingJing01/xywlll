package com.xinya.coldchain.usermanager.model;

public class NwRole {
    private String pkRole;
    private String dr;
    private String ts;
    private String roleCode;
    private String roleName;
    private String pkCrop;
    public String getPkRole() {
        return pkRole;
    }

    public void setPkRole(String pkRole) {
        this.pkRole = pkRole;
    }

    public String getDr() {
        return dr;
    }

    public void setDr(String dr) {
        this.dr = dr;
    }

    public String getTs() {
        return ts;
    }

    public void setTs(String ts) {
        this.ts = ts;
    }

    public String getRoleCode() {
        return roleCode;
    }

    public void setRoleCode(String roleCode) {
        this.roleCode = roleCode;
    }

    public String getRoleName() {
        return roleName;
    }

    public void setRoleName(String roleName) {
        this.roleName = roleName;
    }

    public String getPkCrop() {
        return pkCrop;
    }

    public void setPkCrop(String pkCrop) {
        this.pkCrop = pkCrop;
    }


}
